package Projeto.Endereco;

public class Endereco1 {
	private long id;
	private String lougradoro;
	private int numero;
	private String completemento;
	private String bairro;
	private String cidade;
	private String cep;
	private String uf;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getLougradoro() {
		return lougradoro;
	}
	public void setLougradoro(String lougradoro) {
		this.lougradoro = lougradoro;
	}
	public int getNumero() {
		return numero;
	}
	public void setNumero(int numero) {
		this.numero = numero;
	}
	public String getCompletemento() {
		return completemento;
	}
	public void setCompletemento(String completemento) {
		this.completemento = completemento;
	}
	public String getBairro() {
		return bairro;
	}
	public void setBairro(String bairro) {
		this.bairro = bairro;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getCep() {
		return cep;
	}
	public void setCep(String cep) {
		this.cep = cep;
	}
	public String getUf() {
		return uf;
	}
	public void setUf(String uf) {
		this.uf = uf;
	}
}
